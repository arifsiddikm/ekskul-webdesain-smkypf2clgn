                <b>- Domain</b> <br>
                Domain adalah nama unik yang diberikan untuk mengidentifikasi nama server komputer seperti server web atau server surel di jaringan komputer ataupun internet.
                Contoh : https://youtube.com, https://smkypf2clgn.sch.id. <br><br>
                <b>- Hosting</b> <br>
                Hosting adalah sebuah layanan online yang digunakan untuk mengelola data situs atau aplikasi web oleh pengguna dan ditampilkan melalui via internet. File website yang tersimpan di dalam hosting berupa gambar, script, video, dll. <br><br>
                <b>- Coding/Ngoding</b> <br>
                Coding/Ngoding adalah proses menulis, menguji dan memperbaiki, dan memelihara kode yang membangun suatu program komputer. <br><br>
                <b>- Sintaks</b> <br>
                Syntax adalah sebuah aturan yang digunakan untuk menulis kalimat agar mampu dimengerti oleh bahasa pemrograman, atau simpelnya, sebuah kodingan yang kalian ketik bisa disebut Sintaks. <br><br>
                <b>- Text Editor</b> <br>
                Text editor merupakan sebuah software yang biasanya digunakan oleh seorang programmer dalam menulis sebuah kode program. <br><br>
                <b>- Programming/Pemrograman</b> <br>
                Programming/Pemrograman sederhananya adalah sebuah proses untuk membuat program di komputer. Program yang dibuat bisa berupa software, website, aplikasi android, dsb. <br><br>
                <b>- Bahasa Pemrograman</b> <br>
                Bahasa Pemrograman adalah sebuah instruksi standar untuk memerintah program/komputer. <br>
                Contoh dari Bahasa Pemrograman seperti Javascript, PHP, Python, Pascal, C++, dll.
                Dan untuk HTML, CSS itu ngga bisa disebut bahasa Pemrograman, melainkan Bahasa Markup saja, karena ada beberapa syarat bisa disebut bahasa pemrograman seperti memiliki Variable, Data, dan Function. <br><br>
                <b>- Web Desain/Web Desainer</b> <br>
                Web Desain adalah istilah umum yang digunakan untuk membuat/mendesain bagaimana isi web konten ditampilkan, dan yang merancangnya disebut Web Desainer. <br><br>
                <b>- Web Programming/Web Programmer</b> <br>
                Web Programming ini sama halnya dengan Web Desain, bedanya jika sudah masuk ranah Programming ini, lebih dari sekedar mendesain website, tapi ada aksi untuk merancang processing atau interaksi lebih di sebuah website, dan yang merancangnya disebut Web Programmer. <br><br>
                <b>- Web Development/Web Developer</b> <br>
                Dan untuk Web Development ini, bisa dibilang mengembangkan suatu website lebih luas, jadi tidak hanya sekedar mendesain website, tapi membuat sebuah website menjadi online, dan terus dikembangkan setiap harinya yang bisa digunakan oleh orang orang, dan yang merancangnya disebut Web Developer. <br><br>
                <b>- FrontEnd & BackEnd </b> <br>
                Front end developer adalah orang yang berperan mengembangkan tampilan situs atau aplikasi melalui HTML, CSS, dan JavaScript. Sementara, back end adalah orang yang berperan agar situs atau aplikasi dapat bekerja.
                Sebutan FrontEnd & BackEnd ngga cuma berlaku di Web Dev aja, bisa juga di Android, iOS, dan Software Dev. <br><br>
                <b>- Algoritma</b> <br>
                Algoritma adalah prosedur langkah-demi-langkah untuk penghitungan. Algoritma digunakan untuk penghitungan, pemrosesan data, dan penalaran otomatis. Algoritma hanya sebuah sebutan, biasanya berlaku ketika sedang bekerja di bagian BackEnd. <br><br>
                <b>- Database</b> <br>
                Database adalah kumpulan data yang terorganisir, yang umumnya disimpan dan diakses secara elektronik dari suatu sistem komputer.  <br><br>
                <b>- Web Server</b> <br>
                Web Server adalah perangkat lunak server, atau perangkat keras yang didedikasikan untuk menjalankan perangkat lunak ini, yang dapat memenuhi permintaan klien di World Wide Web.    <br>
                Simpelnya, untuk menjalankan sebuah website yang terdapat Processing Data, dll, membutuhkan sebuah Web Server, contohnya ketika ingin menggunakan Bahasa Pemrograman PHP. <br><br>
                <b>- Responsive Design</b> <br>
                Desain web responsif adalah sebuah metode atau pendekatan sistem web desain yang bertujuan memberikan pengalaman berselancar yang optimal dalam berbagai perangkat, baik mobile maupun komputer meja. <br>
                Berlaku ketika sedang membuat Web menggunakan HTML, CSS, Javascript. <br><br>
                <b>- WireFrame/Mockup/Prototype</b> <br>
                Wireframe adalah sebuah kerangka untuk menata suatu item di laman website atau aplikasi. Pembuatan wireframe biasanya dilakukan sebelum pembuatan produk tersebut dilakukan. Item yang berkaitan seperti teks, gambar, layoiting, dan sebagainya. <br><br>
                <b>- Portfolio/ Web Portfolio</b> <br>
                Portofolio adalah tempat kumpulan hasil karya atau pencapaian yang mempresentasikan pengembangan diri. <br>
                Web Portfolio adalah portfolio digital yang digunakan untuk menunjukkan karya dan bakat mereka secara unik dan kepada audiens yang lebih luas secara online dalam bentuk website. <br><br>
