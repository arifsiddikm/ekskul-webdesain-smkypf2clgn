                <b>Saya ingin mempelajari web programming, harus memulai dari mana?</b><br><br>
                Jawaban singkatnya: mulai dari HTML. <br> <br>
                HTML adalah inti dari seluruh halaman web. Sangat mustahil untuk membuat website tanpa memiliki dasar pengetahuan tentang HTML. Untungnya, HTML juga sangat mudah dipelajari. Anda tidak perlu memiliki dasar programming atau pengetahuan tentang algoritma apapun. Satu-satunya kemampuan yang dibutuhkan adalah anda sudah cukup familiar dengan cara penggunaan web browser seperti Google Chrome atau Mozilla Firefox. <br><br>
                <b>Setelah HTML, lanjut kemana?</b><br><br>
                Jika anda sudah menguasai HTML (minimal dasar-dasar HTML), bisa memilih antara CSS atau PHP. Apabila berminat dengan web design, silahkan lanjut mempelajari CSS. Apabila anda ingin membuat aplikasi web seperti pemrosesan form, menyimpan data ke database, membuat laporan, dll bisa lanjut ke PHP. <br><br>
                <b>Web Programming dengan PHP</b><br><br>
                Berikutnya, setelah mempelajari HTML dan CSS, anda bisa lanjut ke PHP. Berbeda dengan HTML dan CSS, PHP ‘murni’ sebuah bahasa pemrograman komputer. HTML dan CSS sendiri belum bisa dikatakan bahasa pemrograman, tapi lebih ke bahasa kode. Di PHP inilah web programming yang sebenarnya. <br>
                PHP digunakan untuk membuat ‘program’ dari sebuah website. Mulai dari mengatur alur logika, memproses hasil form HTML, hingga berkomunikasi dengan database seperti MySQL. Jika anda membuat sebuah website modern, 80% waktu akan anda habiskan membuat kode program PHP, terutama website kaya fitur seperti sistem informasi, ujian online, portal berita, dll. <br><br>
                <b>Menyimpan Data di Database MySQL</b><br><br>
                Ketika membuat website dengan PHP, kadang kita perlu untuk menyimpan data seperti registrasi user, laporan penjualan, hasil perhitungan, dll. Media penyimpanan ini dikenal dengan database. <br><br>
                <b>Animasi dengan JavaScript</b><br><br>
                Materi terakhir yang perlu dikuasai adalah JavaScript. Sama seperti PHP, JavaScript merupakan bahasa pemrograman murni. <br>
                JavaScript diperlukan untuk membuat halaman website yang interaktif, seperti apa yang terjadi ketika sebuah tombol di-klik, menampilkan pesan kesalahan jika salah mengisi form, membuat animasi hingga game online berbasis web. <br><br>
                <b>HTML + CSS + PHP + MySQL + JavaScript, berapa lama untuk menguasai semua ini?</b><br><br>
                Jawabnya: tergantung <br><br>
                Ada beberapa faktor yang harus dipertimbangkan. Pertama, kata-kata ‘menguasai’ disini sangat relatif. <br>
                Untuk sekedar ‘tahu’ tentang fungsi masing-masing ‘bahasa’ ini, anda bisa meluangkan waktu sekitar 1 minggu (hari pertama belajar HTML, hari kedua belajar CSS, dst). Tapi sekali lagi, ini hanya untuk sekedar ‘tahu’, itu pun jika anda tidak pusing dengan pembahasan masing2nya. <br><br>
