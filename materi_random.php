                Sekilas tentang Programmer, ada beberapa jenis Programmer, selain Web Programmer. <br>
                Yang dimana setiap bidangnya memiliki tujuan dan Bahasa Pemrograman yang berbeda pula.
                <br><br>
                <b>1. Web Programmer</b> <br>
                Programmer paling dikenal adalah web developer. Jenis programmer website ini bertugas untuk membuat website, mulai dari tampilan hingga fitur website. 
                Bahasa pemrograman yang digunakan pada umumnya adalah HTML, CSS, Ruby on Rails, NET, PHP, XML, Java.
                <br><br>
                <b>2. Mobile Programmer</b> <br>
                Untuk Mobile Programmer sendiri bertugas untuk membuat Aplikasi Mobile yang dibagi 2jenis pula, ada Android Programmer dan iOS Programmer untuk iPhone.
                Bahasa pemrograman yang digunakan pada umumnya adalah Java, Python, Swift, DLL.
                <br><br>
                <b>3. Desktop Programmer</b> <br>
                Desktop Programmer bertugas untuk membuat Software pada Komputer, bisa berupa Software untuk OS Windows, Linux, Mac OS, DLL.
                Bahasa pemrograman yang digunakan pada umumnya adalah C++, Java, Python, Swift, DLL.
                <br><br>
                <b>4. Game Programmer</b> <br>
                Game Programmer ini bertugas untuk membuat Game, walau untuk membuat game tidak selalu dengan Programming, ada yang bisa menggunakan Builder Game, tetapi tetap ada yang memerlukan basic dari Game Programming.
                Bahasa pemrograman yang digunakan pada umumnya adalah C++, Java, JavaSript, Python, Swift, DLL.
                <br><br>
                <b>5. System Programmer</b> <br>
                Programmer sistem ini merupakan programmer yang bertugas untuk memasang, menyesuaikan, menjaga sistem operasi agar berjalan dengan baik. Selain itu programmer ini juga bertugas untuk memasang serta melakukan upgrade software yang ada di OS tersebut.
                Bahasa pemrograman yang digunakan pada umumnya adalah Python, Java, R, C++, Scala, DLL.
                <br><br>
                <b>6. Library Programmer</b> <br>
                Programmer library adalah programmer yang membuat fungsi atau program yang bisa dipergunakan kembali. Dengan begitu, Anda tidak perlu lagi membuat program itu sendiri.
                Contohnya sang Pembuat Framework seperti Bootstrap, Tailwind, CodeIgniter, Laravel, DLL.
